class Stop {
  String name = "";
  String value = "";

  Stop(String _name, String _value) {
    name = _name;
    value = _value;
  }

  ({String name, bool is_to_center}) gen_name() {
    return (name: name + " (" + value + ")", is_to_center: to_center());
  }

  bool to_center() {
    if (int.parse(value) % 2 != 0) {
      return true;
    } else {
      return false;
    }
  }

  Stop.fromJson(Map<String, dynamic> json)
      : name = json['name'],
        value = json['value'];

  Map<String, dynamic> toJson() => {
        'name': name,
        'value': value,
      };
}

